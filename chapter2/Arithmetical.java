package tdd.chapter2;

public class Arithmetical {


    public int square1(int num1) {

        return num1 * num1;
    }

    public int square2(int num2) {
        return num2 * num2;
    }

    public int sum(int square1, int square2) {

        return square1 + square2;
    }

    public int difference(int num1, int num2) {
        return  num1 - num2;
    }
}